using System;

namespace Basics
{
    class DataTypes
    {
        public void DataTypes()
        {
            int myInteger = 5; //Whole numbers
            double myDouble = 1.22D; //stores fractions upto 15 decimal digits
            float myFloat = 1.2f; //stores fractions upto 7-8 decimal places
            string myString = "Laviz"; //Stores texts
            char myChar = 'A'; //stores single characters
            bool myBool = true; //stores boolian value(true/false)
            long myLong = 89721; //stores larger whole numbers
            
            Console.WriteLine(myInteger);
            Console.WriteLine(myDouble);
            Console.WriteLine(myFloat);
            Console.WriteLine(myString);
            Console.WriteLine(myChar);
            Console.WriteLine(myBool);
            Console.WriteLine(myLong);
        }

    }

}